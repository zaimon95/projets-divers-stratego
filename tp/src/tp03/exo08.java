public class exo08 {
    public static void main(String[] args) {
        System.out.print("9.99 == 10.0 : ");
        System.out.println(9.99 == 10.0);
        System.out.print("9.999999999999999999999999 == 10.0 : ");
        System.out.println(9.999999999999999999999999 == 10.0);
        System.out.print("9.999999999999999999999999 == 10.00000000000001 : ");
        System.out.println(9.999999999999999999999999 == 10.00000000000001);
        System.out.print("true : ");
        System.out.println(true);
        System.out.print("false : ");
        System.out.println(false);
        System.out.print("!true : ");
        System.out.println(!true);
        System.out.print("!false : ");
        System.out.println(!false);
        System.out.print("true && true : ");
        System.out.println(true && true);
        System.out.print("true && false : ");
        System.out.println(true && false);
        System.out.print("false && true : ");
        System.out.println(false && true);
        System.out.print("false && false : ");
        System.out.println(false && false);
        System.out.print("true || true : ");
        System.out.println(true || true);
        System.out.print("true || false : ");
        System.out.println(true || false);
        System.out.print("false || true : ");
        System.out.println(false || true);
        System.out.print("false || false : ");
        System.out.println(false || false);
    }
}
